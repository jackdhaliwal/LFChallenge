public interface SortChallenge {
	// Takes list in and sorts in ascending order.
	public int[] simpleSort(int[] list, boolean ascending);

	// Takes list in and prints to standard out in ascending order.
	public void printSortedFrequency(int[] list);

	// Takes list in and returns the number of times a unique value appears.
	public int numberOfUniqueValues(int[] list);

}
